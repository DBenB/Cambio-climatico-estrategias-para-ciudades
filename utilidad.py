import random
class Constantes:
    def __init__(self):
        self.delta = 0.1
        self._lambda = 0.1
        self.E_max = 100
        self.T_max = 100
        self.alpha = 1
        self.beta = 1
        self.gamma = 1
class Ciudad:
    def __init__(self,nombre, E_i, T_i, R_i, coop):
        self.nombre = nombre
        self.E_i = E_i
        self.T_i = T_i
        self.R_i = R_i
        self.coop = coop

    def beneficio(self, c):
            return c.delta * (1 - self.E_i/c.E_max ) + self.coop
    def costo(self, c):
            return c._lambda * (1 - self.T_i/c.T_max)
    def utilidad(self,c):
            B_i = self.beneficio(c)
            C_i = self.costo(c)
            return c.alpha*B_i-c.beta*C_i-c.gamma*self.R_i
if __name__ == "__main__":
    c = Constantes()
    n = 11
    ciudades =[Ciudad(f"C {i+1}", E_i=random.uniform(30, 100), T_i=random.uniform(2, 100), R_i=random.uniform(20, 40), coop=random.uniform(0, 10)) for i in range(n)]
    for _ in ciudades:
        print(f"Utilidad de {_.nombre}:{_.utilidad(c)}")
